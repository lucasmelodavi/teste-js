/* Variaveis em JavaScript 
   
  const - constante, não pode ser alterada
  let - variável, pode ser alterada
  var - variável, pode ser alterada, mas não é recomendada para uso

*/

const banana = "fruta"; // constante

console.log(banana); // imprime "fruta"

let banana2 = "fruta"; // variável

banana2 = "cor"; // variável

console.log(banana2); // imprime "cor"