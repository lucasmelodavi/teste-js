/* O que é if?

O if significa:

"Se..."

Ele serve para verificar se uma condição é verdadeira.

O que é else?

O else significa:

"Senão..."

Ele é usado junto com o if.

Dados de Comparação
 Operador  Significado    

 >      Maior que      
 <      Menor que      
 >=     Maior ou igual
 <=      Menor ou igual 
 ===     Igual 
 ==     Igual (não recomendado)         
 !==    Diferente      

 
*/

const notaDoAluno = 5.9

if(notaDoAluno >= 6) {
    console.log("Aluno aprovado")
} else {
    console.log("Aluno reprovado")
}
