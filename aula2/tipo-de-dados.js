/* Tipos de Dados em JavaScript 
    
    string - texto
    number - número
    boolean - verdadeiro(true) ou falso(false)
    array - lista de valores
    object - objeto com propriedades e valores
    Null - valor nulo
    Undefined - valor indefinido

*/

const texto = " exemplo de string"; // string

const numero = 190 / 10; // number

const variavelBooleana = true; // boolean

const usuario1 = [ "João", "Maria", "José" ]; // array

const usuario2 = { nome: "João", idade: 30, solteiro: false, esposa: null }; // object

console.log(numero, texto, variavelBooleana, usuario1[2], usuario2.solteiro); // imprime 19 " exemplo de string" true [1, 2, 3, 4, 5] { nome: "João", idade: 30 }
